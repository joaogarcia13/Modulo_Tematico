                        String modelo = carrinhas.get(i).getModelo();
                        String combustivel = carrinhas.get(i).getCombustivel();
                        String potencia = carrinhas.get(i).getPotencia();
                        String cilindrada = carrinhas.get(i).getCilindrada();
